    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        try{                                          
            // TODO add your handling code here:
            String contra = jt_contra.getText(),nueva="";
            int tem,n,key=Integer.parseInt(jt_key.getText());
            char letra;
            for (int i = 0; i < contra.length(); i++) {
                tem = contra.codePointAt(i);
                if(tem==122){
                    tem=65;
                }
                if(tem==90){
                    tem=97;
                }
                n=tem+key;
                letra = (char)n;
                nueva += letra;
            }
            File archivo = new File("./credenciales.txt");
            FileWriter fw = null;
            BufferedWriter bw = null;
            try{
                fw = new FileWriter(archivo,false);
                bw = new BufferedWriter(fw);
                bw.write(usuario+";");
                bw.write(Integer.toString(key)+";");
                bw.write(nueva+";");
                bw.flush();
            }
            catch(Exception e){
                
            }
            bw.close();
            fw.close();
            
        }
        catch(IOException ex){
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        jmi_login.setEnabled(true);
        jd_crearcontra.dispose();
    }//GEN-LAST:event_jButton10ActionPerformed

