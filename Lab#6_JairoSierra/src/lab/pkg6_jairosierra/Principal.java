public String decifrar(String contra,int key){
    String cifrar="";
    int num;
    for (int i = 0; i < contra.length(); i++) {
        num = contra.codePointAt(i);
        if(num==122){
            num=65;
        }
        if(num==90){
            num=97;
        }
        char tem = (char)(num-key);
        cifrar+=tem;
    }
    return cifrar;
}
