    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        // TODO add your handling code here:
        try {
            String nombre;
            String cantidad;
            String anos;
            String region;
            int vivas;
            nombre = jt_nomcria.getText();
            cantidad = jt_energia.getText();
            anos = jt_anos.getText();
            region = jt_region.getText();
            vivas = Integer.parseInt(jt_vivas.getText());
            ac.getListamundos().get(jl_mundos.getSelectedIndex()).getCriaturas().add(new Criaturas(nombre, cantidad, anos, region, vivas));
            ac.escribirarchivo();
            ac.cargararchivo();
            DefaultListModel modelo = (DefaultListModel) jl_criaturas.getModel();
            for (int i = 0; i < ac.getListamundos().get(jl_mundos.getSelectedIndex()).getCriaturas().size(); i++) {
                modelo.addElement(ac.getListamundos().get(jl_mundos.getSelectedIndex()).getCriaturas().get(i));
                jl_criaturas.setModel(modelo);
            }
            jt_nomcria.setText("");
            jt_energia.setText("");
            jt_anos.setText("");
            jt_region.setText("");
            jt_vivas.setText("");
            jd_crearcri.dispose();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(jd_crearcri, "Hay errores en los datos!! Corrija!!");
        }
    }//GEN-LAST:event_jButton6MouseClicked

